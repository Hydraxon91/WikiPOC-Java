CREATE TABLE style_model (
                             id INT IDENTITY PRIMARY KEY,
                             logo VARCHAR(255),
                             wiki_name VARCHAR(255),
                             body_color VARCHAR(255),
                             article_right_color VARCHAR(255),
                             article_right_inner_color VARCHAR(255),
                             article_color VARCHAR(255),
                             footer_list_link_text_color VARCHAR(255),
                             footer_list_text_color VARCHAR(255),
                             font_family VARCHAR(255)
);
